    private void getData() {
        DefaultTableModel model = (DefaultTableModel) table_Hasil.getModel();
        model.setRowCount(0); // Mengosongkan tabel sebelum menambahkan data baru
        
        try {
            String sql = "SELECT * FROM table_hasil";
            PreparedStatement ps = conn.prepareStatement(sql); 
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()) {
                int kd_hasil = rs.getInt("kd_hasil");
                int kd_arsip = rs.getInt("kd_arsip"); // Mengambil nilai dari kolom kd_arsip
                String no_surat = rs.getString("no_surat"); // Mengambil nilai dari kolom arsip
                String kd_progres = rs.getString("no_surat"); // Mengambil nilai dari kolom arsip
                String tgl_jawab = rs.getString("tgl_jawab"); // Mengambil nilai dari kolom arsip
                String hasil = rs.getString("hasil"); // Mengambil nilai dari kolom arsip
                String kd_penyakit = rs.getString("kd_penyakit"); // Mengambil nilai dari kolom arsip
                String jml_diagnosa = rs.getString("jml_diagnosa"); // Mengambil nilai dari kolom arsip
                    
                Object[] rowData = {kd_hasil, kd_arsip, no_surat, kd_progres, tgl_jawab, hasil, kd_penyakit, jml_diagnosa}; // Membuat array objek untuk baris data
                model.addRow(rowData); // Menambahkan baris data ke dalam tabel
            
            }
            rs.close();
            ps.close();
        } catch (Exception e) {
            Logger.getLogger(ArsipView.class.getName()).log(Level.SEVERE, null, e);
        }
    }
