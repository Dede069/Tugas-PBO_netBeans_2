    private void btn_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SimpanActionPerformed
        // TODO add your handling code here:
        String kd_hasil = t_KdHasil.getText();
        String kd_arsip = t_Arsip.getText();
        String no_surat = t_Surat.getText();
        String kd_progres = t_Progres.getText();
        String tgl_jawab = t_Tanggal.getText();
        String hasil = t_Hasil.getText();
        String kd_penyakit = t_Penyakit.getText();
        String jml_diagnosa = t_Diagnosa.getText();
        
        if(kd_hasil.isEmpty() || kd_arsip.isEmpty() || no_surat.isEmpty() || kd_progres.isEmpty() || tgl_jawab.isEmpty() || hasil.isEmpty() || kd_penyakit.isEmpty() || jml_diagnosa.isEmpty()) {
        JOptionPane.showMessageDialog(this, "semua kolum haris di sisi", "validasi", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        try {
            String sql = "insert into table_hasil (kd_hasil,kd_arsip,no_surat,kd_progres,tgl_jawab,hasil,kd_penyakit,jml_diagnosa) VALUES (?,?,?,?,?,?,?,?)";
            try (PreparedStatement st = conn.prepareStatement(sql)) {
                st.setString(1, kd_hasil);
                st.setString(2, kd_arsip);
                st.setString(3, no_surat);
                st.setString(4, kd_progres);
                st.setString(5, tgl_jawab);
                st.setString(6, hasil);
                st.setString(7, kd_penyakit);
                st.setString(8, jml_diagnosa);
                
                int rowInserted = st.executeUpdate();
                if(rowInserted >0) {
                    JOptionPane.showMessageDialog(this, "data berhasil ditambahkan");
                    resetForm();
                    getData();
                }
            }
        } catch (Exception e) {
            Logger.getLogger(ArsipView.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btn_SimpanActionPerformed

    private void btn_UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UbahActionPerformed
        // TODO add your handling code here:
     int selectedRow = table_Hasil.getSelectedRow();
        
     if(selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "pilih baris yang akan dipergarui");
        return;
        }    
    String kd_hasil = table_Hasil.getValueAt(selectedRow, 0).toString();
    String kd_arsip = t_Arsip.getText();
    String no_surat = t_Surat.getText();
    String kd_progres = t_Progres.getText();
    String tgl_jawab = t_Tanggal.getText();
    String hasil = t_Hasil.getText();
    String kd_penyakit = t_Penyakit.getText();
    String jml_diagnosa = t_Diagnosa.getText();
    
    if(kd_hasil.isEmpty() || kd_arsip.isEmpty() || no_surat.isEmpty() || kd_progres.isEmpty() || tgl_jawab.isEmpty() || hasil.isEmpty() || kd_penyakit.isEmpty() || jml_diagnosa.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Semua kolom harus diisi", "Validasi", JOptionPane.ERROR_MESSAGE);
        return;
        }
    
        try {
            String sql = "UPDATE INTO table_hasil SET kd_arsip=?, no_surat=?, kd_progres=?, tgl_jawab=?, hasil=?, kd_penyakit=?, jml_diagnosa=? WHERE kd_hasil=?";
             PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, kd_arsip);
            st.setString(2, no_surat);
            st.setString(3, kd_progres);
            st.setString(4, tgl_jawab);
            st.setString(5, hasil);
            st.setString(6, kd_penyakit);
            st.setString(7, jml_diagnosa);
            st.setString(8, kd_hasil);
            
            int rowUpdate = st.executeUpdate();
            if(rowUpdate > 0) {
                JOptionPane.showMessageDialog(this, "data berhasil diubah");
            resetForm();
            getData();
            }
            st.close();
        } catch (HeadlessException | SQLException e) {
            Logger.getLogger(ArsipView.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btn_UbahActionPerformed

