    private void btn_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SimpanActionPerformed
        // TODO add your handling code here:
        String kd_hewan = t_KodeHewa.getText();
        String hewan = t_Hewan.getText();
        
        if(kd_hewan.isEmpty() || hewan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "semua kolum haris di sisi", "validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            String sql = "insert into table_hewan (kd_hewan,hewan) VALUES (?,?)";
            try (PreparedStatement st = conn.prepareStatement(sql)) {
                st.setString(1, kd_hewan);
                st.setString(2, hewan);
                
                int rowInserted = st.executeUpdate();
                if(rowInserted >0) {
                    JOptionPane.showMessageDialog(this, "data berhasil ditambahkan");
                    resetForm();
                    getData();
                }
            }
        } catch (SQLException e) {
            Logger.getLogger(ArsipView.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btn_SimpanActionPerformed

    private void btn_UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UbahActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_Hewan.getSelectedRow();
        if(selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "pilih baris yang akan dipergarui");
        return;
        }
        
        String kd_hewan =tbl_Hewan.getValueAt(selectedRow, 0).toString();
        String hewan = t_Hewan.getText();
        
        if(hewan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "semua kolom harus diisi", "validasi", JOptionPane.ERROR_MESSAGE);
        return;
            }
        
        try {
        String sql = "UPDATE table_hewan SET hewan=? WHERE kd_hewan=?";
        PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, hewan);
            st.setString(2, kd_hewan);
            
            int rowUpdate = st.executeUpdate();
            if(rowUpdate > 0) {
                JOptionPane.showMessageDialog(this, "data berhasil diubah");
                resetForm();
                getData();
            }
            
            st.close();
        } catch (HeadlessException | SQLException e) {
            Logger.getLogger(ArsipView.class.getName()).log(Level.SEVERE, null, e);
        } 
    }//GEN-LAST:event_btn_UbahActionPerformed

