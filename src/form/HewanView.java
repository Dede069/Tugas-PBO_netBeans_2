    private void getData() {
        DefaultTableModel model = (DefaultTableModel) tbl_Hewan.getModel();
        model.setRowCount(0); // Mengosongkan tabel sebelum menambahkan data baru
        
        try {
            String sql = "SELECT * FROM table_hewan";
            PreparedStatement ps = conn.prepareStatement(sql); 
            ResultSet rs = ps.executeQuery();
                
            while (rs.next()) {
                int kd_hewan = rs.getInt("kd_hewan"); // Mengambil nilai dari kolom kd_arsip
                String hewan = rs.getString("hewan"); // Mengambil nilai dari kolom arsip
                    
                Object[] rowData = {kd_hewan, hewan}; // Membuat array objek untuk baris data
                model.addRow(rowData); // Menambahkan baris data ke dalam tabel
                
            }
            rs.close();
            ps.close();
        } catch (SQLException e) {
            Logger.getLogger(ArsipView.class.getName()).log(Level.SEVERE, null, e);
        }
    }
